import Foundation

/// The purpose of this enum is to store the possible values for rating CE activities
enum ActivityRating: String, CaseIterable {
    case terrible = "Terrible"
    case poor = "Poor"
    case soSo = "So - so"
    case interesting = "Interesting"
    case lovedIt = "Loved it"
}
